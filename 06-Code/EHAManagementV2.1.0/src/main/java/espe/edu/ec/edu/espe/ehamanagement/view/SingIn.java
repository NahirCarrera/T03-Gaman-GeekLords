    public void validateFields() {
        
        if (txtfNameEmail.getText().equals("name@gmail.com")) {
            lbEmailValidation.setText("this area is required");
            pictureWarningEmail();
        } else {
            if (txtfNameEmail.getText().equals("")) {
                pictureWhiteEmail();
                lbEmailValidation.setText("");

            } else {

                if (!txtfNameEmail.getText().contains("@") || !txtfNameEmail.getText().contains(".")) {
                    lbEmailValidation.setText("invalid mail");
                    pictureWarningEmail();
                } else {
                    lbEmailValidation.setText("");
                    pictureWhiteEmail();
                }
            }

        }
        if (txtNamePassword.getText().equals("***************")) {
            lbPasswordValidation.setText("this area is required");
            pictureWarningPassword();
        } else {
            lbPasswordValidation.setText("");
            pictureWhitePassword();
        }

        if (txtfNameCurrentSalary.getText().equals("write your current salary ") || txtNamePassword.getText().equals("***************") || lbEmailValidation.getText().equals("invalid mail")
                || txtfNameEmail.getText().equals("name@gmail.com") || txtfNameUser.getText().equals("Enter your username ") || txtfNameUser.getText().isEmpty() || txtNamePassword.getText().isEmpty() || txtfNameEmail.getText().isEmpty()
                || txtfNameCurrentSalary.getText().isEmpty()) {
            btnSend.setEnabled(false);
        } else {
            btnSend.setEnabled(true);
        }

    }

