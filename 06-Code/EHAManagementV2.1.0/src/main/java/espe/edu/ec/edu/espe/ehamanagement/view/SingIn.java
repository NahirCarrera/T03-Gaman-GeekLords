    private void txtfNameUserKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfNameUserKeyTyped
        char c = evt.getKeyChar();
        if (txtfNameUser.getText().equals("Enter your username ")) {
            lbUserValidation.setText("this area is required");
            pictureWarningUser();
        } else 
        {
            if (!Character.isLetter(c) && (c != KeyEvent.VK_BACK_SPACE && c != KeyEvent.VK_SPACE)) {
            //getToolkit().beep();
            evt.consume();
            pictureWarningUser();
            lbUserValidation.setText(c + " is not accepted here!");
            
        } else {
            lbUserValidation.setText("");
            pictureWhiteUser();
        }
            
            
        }
        


    }//GEN-LAST:event_txtfNameUserKeyTyped

